#!/usr/bin/env php
<?php

/**
 * This script generates a phpunit.xml file that can be used to run PHPUnit on both MW core and every
 * installed extension and skin. It works by reading a list of extensions and skins from the settings files,
 * and then adding everything it finds to a new phpunit.xml file, which is excluded from version control.
 * The config file is based on phpunit.xml.template (included in version control).
 */

use MediaWiki\HookContainer\HookRunner;
use MediaWiki\MediaWikiServices;
use PHPUnit\Util\Xml\Loader as XmlLoader;

require_once __DIR__ . '/../../vendor/autoload.php';
require_once __DIR__ . '/bootstrap.common.php';

echo "Generating PHPUnit config\n";

TestSetup::loadSettingsFiles();

$extRegistry = ExtensionRegistry::getInstance();

// Build a list of extension tests, based on autodiscovered tests as well as tests added with the
// UnitTestsList hook.
$extTestPaths = [];
foreach ( $extRegistry->getAllThings() as $extInfo ) {
	$extTestPaths[] = dirname( $extInfo['path'] ) . '/tests/phpunit';
}
// Extensions can return a list of files or directories
( new HookRunner( MediaWikiServices::getInstance()->getHookContainer() ) )->onUnitTestsList( $extTestPaths );
$extTestPaths = array_unique( $extTestPaths );

$extUnitTestPaths = [];
$extensionsPaths = array_map( 'dirname', array_keys( $extRegistry->getQueue() ) );
foreach ( $extensionsPaths as $componentPath ) {
	$extUnitTestPaths[] = "$componentPath/tests/phpunit/unit";
}

$baseConfigPath = __DIR__ . '/../../phpunit.xml.template';
$outConfigPath = __DIR__ . '/../../phpunit.xml';

$config = ( new XmlLoader )->loadFile( $baseConfigPath, false, true, true );
$xpath = new DOMXPath( $config );

$comment = $config->firstChild;
if ( !str_contains( $comment->nodeValue, 'composer phpunit:config' ) ) {
	throw new RuntimeException( 'Warning comment has been moved?' );
}
$comment->nodeValue = "\nThis file is generated by tests/phpunit/generatePHPUnitConfig.php.\n" .
	"Any changes you make will be lost upon running the script.\nThis file is also excluded from version control.\n";

$addNodeFromPath = static function ( DOMNode $parent, string $path ) use ( $config ): void {
	if ( is_dir( $path ) ) {
		$nodeType = 'directory';
	} elseif ( is_file( $path ) ) {
		$nodeType = 'file';
	} else {
		// Doesn't exist. Skip.
		return;
	}
	$node = $config->createElement( $nodeType, $path );
	$parent->appendChild( $node );
};

$extensionsSuite = $xpath->query( '//testsuite[@name="extensions"]' )->item( 0 );
foreach ( $extTestPaths as $extPath ) {
	$addNodeFromPath( $extensionsSuite, $extPath );
}

$extensionsUnitSuite = $xpath->query( '//testsuite[@name="extensions:unit"]' )->item( 0 );
foreach ( $extUnitTestPaths as $extUnitPath ) {
	$addNodeFromPath( $extensionsUnitSuite, $extUnitPath );
}

$config->formatOutput = true;
$newContent = $config->saveXML();

$file = fopen( $outConfigPath, 'cb+' );
// Acquire an exclusive lock and avoid unnecessary writes to prevent race conditions where this script is run by
// multiple processes. `file_put_contents` with LOCK_EX would be equivalent, except for the "unnecessary writes" part,
// but it would requires us to call `flock` for all readers, which may be impossible considering that PHPUnit itself
// will need to open the file. (T419107)
flock( $file, LOCK_EX );

// Avoid pointless read of empty file
$fileSize = filesize( $outConfigPath );
if ( $fileSize === 0 ) {
	$needsWrite = true;
} else {
	$oldContent = fread( $file, $fileSize );
	$needsWrite = $newContent !== $oldContent;
}

if ( $needsWrite ) {
	ftruncate( $file, 0 );
	rewind( $file );
	fwrite( $file, $newContent );
	fflush( $file );
	echo "Config written to " . realpath( $outConfigPath ) . "\n";
} else {
	echo "Config already up-to-date.\n";
}
fclose( $file );
