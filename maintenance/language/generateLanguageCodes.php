require_once( dirname( __FILE__ ) . '/../commandLine.inc' );

$optionsWithArgs = array( 'codelist' );

$usage = 'Generate/update a PHP array of language codes.
Outputs directly to LanguageCodes.php in directory of this script.
That file should be put in the languages directory.

Usage: php generateLanguageList.php --codelist="PathOrUrl"

Options:
  --help            Show this help text
  --codelist        the file with codes, from SIL.org e.g. http://sil.org/iso639-3/iso-639-3_20110525.tab
';

if ( isset( $options['help'] ) || !isset( $options['codelist'] ) ) {
	die( $usage );
}

$file = file( $options['codelist'] );

if( !$file ) {
	die( 'Invalid file: ' . $options['codelist'] );
}

$dataNames = explode("\t", $file[0] );
$dataNames[0] = 'Id'; # hack to avoid BOM

unset( $file[0] );

$codeList = array();
$codeListRedirects = array();

foreach( $file as $line ) {
	$exploded = explode( "\t", trim( $line ) );
	foreach( $exploded as $i => $value) {
		if( $value ) {
			$name = trim( $dataNames[$i] );
			$codeList['tmp'][$name] = $value;
		}
	}
	$index = $codeList['tmp']['Id']; # the ISO 639-3 language code

	$part1 = isset( $codeList['tmp']['Part1'] ) ? $codeList['tmp']['Part1'] : null;
	if( $part1 ) {
		# If there is an ISO 639-1, make the ISO 639-1 refer to the ISO 639-2
		$codeListRedirects[$part1] = $index;
	}

	$part2B = isset( $codeList['tmp']['Part2B'] ) ? $codeList['tmp']['Part2B'] : null;
	if( $part2B ) {
		# If there is an ISO 639-2B, make the ISO 639-1 refer to the ISO 639-2
		$codeListRedirects[$part2B] = $index;
	}

	$part2T = isset( $codeList['tmp']['Part2T'] ) ? $codeList['tmp']['Part2T'] : null;
	if( $part2T ) {
		# If there is an ISO 639-2T, make the ISO 639-2T refer to the ISO 639-2
		$codeListRedirects[$part2T] = $index;
	}

	$codeList[$index] = $codeList['tmp'];
	unset( $codeList['tmp'] );
}

$str = '<?php
/**
 * This file by was generated by generateLanguageCodes.php on ' . date('Y-m-d') . '
 */

$wgISO6393LanguageCodes = ' . var_export( $codeList, true ) . ';

$wgISO6393LanguageCodeRedirects = ' . var_export( $codeListRedirects, true ) . ';
';

$fp = fopen( dirname( __FILE__ ) . '/LanguageCodes.php', 'w' );
fwrite( $fp, $str );
fclose( $fp );
