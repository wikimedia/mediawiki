/**
 * A title formatter service for MediaWiki.
 *
 * This is designed to encapsulate knowledge about a given conventions for the title
 * forms to be used in the database, in urls, in wikitext, etc.
 *
 * @see https://www.mediawiki.org/wiki/Requests_for_comment/TitleValue
 */
interface TitleFormatter {

	/**
	 * Returns the formatted title.
	 *
	 * @param TitleValue $title
	 *
	 * @return string
	 */
	//TODO: flags: with/without namespace, etc //DO NOT MERGE without this
	public function format( TitleValue $title );

	/**
	 * Returns a value identifying the target form generated by this formatter.
	 *
	 * @return int See the TitleValue:XXX_FORM constants.
	 */
	public function getTargetForm();
}